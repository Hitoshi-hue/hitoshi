#include "libft.h"

char	*ft_strcpy(char *ptr, const char *src)
{
	size_t i;

	i = 0;
	while (src[i] != '\0')
	{
		ptr[i] = src[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
